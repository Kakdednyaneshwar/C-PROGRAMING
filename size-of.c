//A program for size of data types...
#include <stdio.h>
#include<limits.h>

int main()
{
    printf("Size of int is=%ld\n",sizeof(int));
    printf("Size of char is=%ld\n",sizeof(char));
    printf("Size of float is=%ld\n",sizeof(float));
    printf("Size of double is=%ld",sizeof(double));

    return 0;
}
